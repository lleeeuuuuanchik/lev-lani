// ─── Плагин инициализации темы ────────────────────────────────────────────────
// Файл с суффиксом .client.ts — выполняется только в браузере.
// Запускается при первом монтировании приложения, до рендера компонентов.
// Читает тему из localStorage и устанавливает data-theme на <html>,
// чтобы CSS-переменные (--accent, --champagne и т.д.) применились сразу —
// без мигания дефолтной темы при загрузке.

import { useTheme } from '@/composables/useTheme';

export default defineNuxtPlugin(() => {
	const { initTheme } = useTheme();
	initTheme(); // применяет сохранённую тему (или 'rose' по умолчанию)
});
